function hover1(){
    document.getElementById("item-1").style.backgroundColor="#673FD9";
    document.getElementById("item-1").style.borderRadius="3px";
    document.getElementById("item-1").style.cursor = 'pointer';
}
function unhover(){
    document.getElementById("item-1").style.backgroundColor="transparent";
    document.getElementById("item-1").style.borderRadius="0";
    document.getElementById("item-1").style.cursor = 'pointer';
}
